import 'dart:math';

import 'package:flutter/material.dart';

String namanim(String nama, String nim) {
  return "$nama, $nim";
}

void main() => runApp(MyApp());

class MyApp extends StatefulWidget {
  @override
  State<StatefulWidget> createState() {
    // TODO: implement createState
    return MyAppState();
  }
}

int random_RGB() {
  Random rgb = new Random();
  var hasil = rgb.nextInt(255);
  return hasil;
}

class MyAppState extends State<MyApp> {
  String textHolder = "Perubahan Warna";
  int rgb1 = random_RGB();
  int rgb2 = random_RGB();
  int rgb3 = random_RGB();
  int rgb4 = random_RGB();
  
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      debugShowCheckedModeBanner: false,
      home: Scaffold(
        appBar: AppBar(
          title: Text('Flutter UI Succinctly'),
        ),
        body: Container(
            child: Text(
              textHolder,
              style: TextStyle(fontSize: 24),
          ),
        ),
        floatingActionButton: FloatingActionButton(
          child: Icon(Icons.add_alert, color: Color.fromARGB(34, 24, 25, 45)),
          onPressed: () {
            setState(() {
              textHolder = namanim("Deby Zamzam Firdaus", "2020573010015");
              rgb1 = random_RGB();
              rgb2 = random_RGB();
              rgb3 = random_RGB();
              rgb4 = random_RGB();
            });
          },
        ),
      ),
      theme: ThemeData(
        primaryColor: Color.fromARGB(255, 250, 41, 246),
        appBarTheme: AppBarTheme(color: Color.fromARGB(rgb1, rgb2, rgb3, rgb4)),
        accentColor: Color.fromARGB(rgb1, rgb2, rgb3, rgb4),
        textTheme: const TextTheme(
          bodyText2: TextStyle(
            fontSize: 26, fontStyle: FontStyle.italic),
        ),
      ),
    );
  }
}